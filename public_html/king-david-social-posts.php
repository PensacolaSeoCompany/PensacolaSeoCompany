        btn.classList.add('copied');
        span.textContent = 'Copied!';
        setTimeout(function () {
            btn.classList.remove('copied');
            span.textContent = orig;
        }, 2000);
    }).catch(function () {
        el.select();
        document.execCommand('copy');
    });
}
