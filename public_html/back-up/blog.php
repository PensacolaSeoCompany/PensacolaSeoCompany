                                <i class="bi bi-chevron-left"></i> Prev
                            </a>
                        </li>
                    <?php endif; ?>

                    <!-- Page Numbers -->
                    <?php
                    // Show limited page numbers with ellipsis
                    $startPage = max(1, $currentPage - 2);
