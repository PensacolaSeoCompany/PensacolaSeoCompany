        <?php
        // Pagination settings
        $articlesPerPage = 10;
        $currentPage = isset($_GET['page']) ? max(1, intval($_GET['page'])) : 1;

        // Get all articles (match article filename patterns)
        $articlePatterns = [
            __DIR__ . '/how-to-*.php',
            __DIR__ . '/the-*.php',
            __DIR__ . '/best-*.php',
            __DIR__ . '/building-*.php',
            __DIR__ . '/creating-*.php',
            __DIR__ . '/effective-*.php',
            __DIR__ . '/tips-*.php',
            __DIR__ . '/troubleshooting-*.php',
            __DIR__ . '/understanding-*.php',
        ];
        $allArticles = [];
        foreach ($articlePatterns as $pattern) {
            $allArticles = array_merge($allArticles, glob($pattern));
        }

        // Sort articles alphabetically by filename
        sort($allArticles);

        // Calculate pagination
        $totalArticles = count($allArticles);
        $totalPages = ceil($totalArticles / $articlesPerPage);
        $currentPage = min($currentPage, $totalPages); // Don't exceed total pages
        $offset = ($currentPage - 1) * $articlesPerPage;

        // Get articles for current page
        $articles = array_slice($allArticles, $offset, $articlesPerPage);
        ?>

        <div class="d-flex justify-content-between align-items-center mb-4">
            <h2 class="h4 mb-0"><i class="bi bi-journal-text me-2"></i>All Articles</h2>
            <span class="page-info"><?php echo $totalArticles; ?> articles total</span>
        </div>

        <div class="row">
            <?php foreach ($articles as $articlePath):
                $filename = basename($articlePath, '.php');

                // Create a readable title from filename
                $title = str_replace('-', ' ', $filename);
                $title = ucwords($title);

                // Truncate long titles
                $displayTitle = (strlen($title) > 60) ? substr($title, 0, 57) . '...' : $title;

                $url = '/' . $filename;

                // Extract meta description from article file for excerpt
                $excerpt = '';
                $articleContent = file_get_contents($articlePath);
                if (preg_match('/<meta name="description" content="([^"]*)"/', $articleContent, $matches)) {
                    $excerpt = $matches[1];
                    // Clean up and truncate excerpt
                    $excerpt = strip_tags($excerpt);
                    if (strlen($excerpt) > 120) {
                        $excerpt = substr($excerpt, 0, 117) . '...';
                    }
                }

                // Determine category based on filename keywords
                $category = 'SEO';
                if (strpos($filename, 'wordpress') !== false) {
                    $category = 'WordPress';
                } elseif (strpos($filename, 'link-building') !== false || strpos($filename, 'backlink') !== false) {
                    $category = 'Link Building';
                } elseif (strpos($filename, 'local-seo') !== false || strpos($filename, 'google-business') !== false || strpos($filename, 'google-my-business') !== false) {
                    $category = 'Local SEO';
                } elseif (strpos($filename, 'technical') !== false || strpos($filename, 'schema') !== false || strpos($filename, '404') !== false || strpos($filename, 'audit') !== false) {
                    $category = 'Technical SEO';
                } elseif (strpos($filename, 'content') !== false || strpos($filename, 'blog') !== false) {
                    $category = 'Content';
                } elseif (strpos($filename, 'social-media') !== false) {
                    $category = 'Social Media';
                } elseif (strpos($filename, 'analytics') !== false) {
                    $category = 'Analytics';
                } elseif (strpos($filename, 'roofing') !== false || strpos($filename, 'roof') !== false) {
                    $category = 'Industry';
                }
            ?>
                <div class="col-12 col-md-6 mb-4">
                    <div class="card h-100 article-card hover-shadow-lg">
                        <div class="card-body d-flex flex-column">
                            <span class="article-category"><?php echo $category; ?></span>
                            <h5 class="card-title">
                                <a href="<?php echo $url; ?>"><?php echo htmlspecialchars($displayTitle); ?></a>
                            </h5>
                            <?php if ($excerpt): ?>
                                <p class="article-excerpt"><?php echo htmlspecialchars($excerpt); ?></p>
                            <?php endif; ?>
                            <div class="article-meta">
                                <?php include 'author-card-small.php'; ?>
                                <a href="<?php echo $url; ?>" class="btn btn-sm btn-outline-primary ms-auto">Read</a>
                            </div>
                        </div>
                    </div>
                </div>
            <?php endforeach; ?>
        </div>

